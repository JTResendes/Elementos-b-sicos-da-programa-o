"""Lê o número de horas de um empregado
"""
if __name__ == '__main__':
    horas = int(input("Digite o numero de horas"))
    sporhora = int(input("Salário por hora"))

if horas < 40:
    salario = sporhora * horas
    print(f'Salário {salario}')
else:
    horas > 40
    salario = (sporhora * horas)

    print(f'Salário {salario}')
